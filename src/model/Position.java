package model;

public class Position {

	private int posNo;
	private String posNm;
	private String posIn;
	private String posUp;
	private String posDe;
	public int getPosNo() {
		return posNo;
	}
	public void setPosNo(int posNo) {
		this.posNo = posNo;
	}
	public String getPosNm() {
		return posNm;
	}
	public void setPosNm(String posNm) {
		this.posNm = posNm;
	}
	public String getPosIn() {
		return posIn;
	}
	public void setPosIn(String posIn) {
		this.posIn = posIn;
	}
	public String getPosUp() {
		return posUp;
	}
	public void setPosUp(String posUp) {
		this.posUp = posUp;
	}
	public String getPosDe() {
		return posDe;
	}
	public void setPosDe(String posDe) {
		this.posDe = posDe;
	}
	
	
}
