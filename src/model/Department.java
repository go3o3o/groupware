package model;

public class Department {

	private String deptNO;
	private String brNo;
	private String deptNm;
	private String empNo;
	private String deptIn;
	private String deptUp;
	private String deptDe;
	public String getDeptNO() {
		return deptNO;
	}
	public void setDeptNO(String deptNO) {
		this.deptNO = deptNO;
	}
	public String getBrNo() {
		return brNo;
	}
	public void setBrNo(String brNo) {
		this.brNo = brNo;
	}
	public String getDeptNm() {
		return deptNm;
	}
	public void setDeptNm(String deptNm) {
		this.deptNm = deptNm;
	}
	public String getEmpNo() {
		return empNo;
	}
	public void setEmpNo(String empNo) {
		this.empNo = empNo;
	}
	public String getDeptIn() {
		return deptIn;
	}
	public void setDeptIn(String deptIn) {
		this.deptIn = deptIn;
	}
	public String getDeptUp() {
		return deptUp;
	}
	public void setDeptUp(String deptUp) {
		this.deptUp = deptUp;
	}
	public String getDeptDe() {
		return deptDe;
	}
	public void setDeptDe(String deptDe) {
		this.deptDe = deptDe;
	}
	
}
