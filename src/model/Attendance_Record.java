package model;

public class Attendance_Record {
	private int atdSeq;
	private String empNo;
	private String atdDate;
	private String atdOffTime;
	private String atdOnTime;
	private String idAddr;
	private String atdSt;
	public int getAtdSeq() {
		return atdSeq;
	}
	public void setAtdSeq(int atdSeq) {
		this.atdSeq = atdSeq;
	}
	public String getEmpNo() {
		return empNo;
	}
	public void setEmpNo(String empNo) {
		this.empNo = empNo;
	}
	public String getAtdDate() {
		return atdDate;
	}
	public void setAtdDate(String atdDate) {
		this.atdDate = atdDate;
	}
	public String getAtdOffTime() {
		return atdOffTime;
	}
	public void setAtdOffTime(String atdOffTime) {
		this.atdOffTime = atdOffTime;
	}
	public String getAtdOnTime() {
		return atdOnTime;
	}
	public void setAtdOnTime(String atdOnTime) {
		this.atdOnTime = atdOnTime;
	}
	public String getIdAddr() {
		return idAddr;
	}
	public void setIdAddr(String idAddr) {
		this.idAddr = idAddr;
	}
	public String getAtdSt() {
		return atdSt;
	}
	public void setAtdSt(String atdSt) {
		this.atdSt = atdSt;
	}
	
	
}
