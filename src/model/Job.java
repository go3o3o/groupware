package model;

public class Job {

	private int jobNo;
	private String jobNm;
	private String jobIn;
	private String jobUp;
	private String jobDe;
	public int getJobNo() {
		return jobNo;
	}
	public void setJobNo(int jobNo) {
		this.jobNo = jobNo;
	}
	public String getJobNm() {
		return jobNm;
	}
	public void setJobNm(String jobNm) {
		this.jobNm = jobNm;
	}
	public String getJobIn() {
		return jobIn;
	}
	public void setJobIn(String jobIn) {
		this.jobIn = jobIn;
	}
	public String getJobUp() {
		return jobUp;
	}
	public void setJobUp(String jobUp) {
		this.jobUp = jobUp;
	}
	public String getJobDe() {
		return jobDe;
	}
	public void setJobDe(String jobDe) {
		this.jobDe = jobDe;
	}
	
}
