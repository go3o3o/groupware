package model;

public class MeetingRoom {

	private String mtNo;
	private String brNo;
	private String empNo;
	private String mtNm;
	private String mtStart;
	private String mtEnd;
	public String getMtNo() {
		return mtNo;
	}
	public void setMtNo(String mtNo) {
		this.mtNo = mtNo;
	}
	public String getBrNo() {
		return brNo;
	}
	public void setBrNo(String brNo) {
		this.brNo = brNo;
	}
	public String getEmpNo() {
		return empNo;
	}
	public void setEmpNo(String empNo) {
		this.empNo = empNo;
	}
	public String getMtNm() {
		return mtNm;
	}
	public void setMtNm(String mtNm) {
		this.mtNm = mtNm;
	}
	public String getMtStart() {
		return mtStart;
	}
	public void setMtStart(String mtStart) {
		this.mtStart = mtStart;
	}
	public String getMtEnd() {
		return mtEnd;
	}
	public void setMtEnd(String mtEnd) {
		this.mtEnd = mtEnd;
	}

	
}
