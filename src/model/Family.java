package model;

public class Family {
	private String empNO;
	private String famType;
	private String famNm;
	private String famBirth;
	private String famRemarks;
	public String getEmpNO() {
		return empNO;
	}
	public void setEmpNO(String empNO) {
		this.empNO = empNO;
	}
	public String getFamType() {
		return famType;
	}
	public void setFamType(String famType) {
		this.famType = famType;
	}
	public String getFamNm() {
		return famNm;
	}
	public void setFamNm(String famNm) {
		this.famNm = famNm;
	}
	public String getFamBirth() {
		return famBirth;
	}
	public void setFamBirth(String famBirth) {
		this.famBirth = famBirth;
	}
	public String getFamRemarks() {
		return famRemarks;
	}
	public void setFamRemarks(String famRemarks) {
		this.famRemarks = famRemarks;
	}
	
	
	
	
}
