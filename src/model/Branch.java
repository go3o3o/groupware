package model;

public class Branch {
	private String comNo;
	private String brNo;
	private String brNm;
	private String brCountry;
	private String brLocation;
	private String brIn;
	private String brUp;
	private String brDe;
	public String getComNo() {
		return comNo;
	}
	public void setComNo(String comNo) {
		this.comNo = comNo;
	}
	public String getBrNo() {
		return brNo;
	}
	public void setBrNo(String brNo) {
		this.brNo = brNo;
	}
	public String getBrNm() {
		return brNm;
	}
	public void setBrNm(String brNm) {
		this.brNm = brNm;
	}
	public String getBrCountry() {
		return brCountry;
	}
	public void setBrCountry(String brCountry) {
		this.brCountry = brCountry;
	}
	public String getBrLocation() {
		return brLocation;
	}
	public void setBrLocation(String brLocation) {
		this.brLocation = brLocation;
	}
	public String getBrIn() {
		return brIn;
	}
	public void setBrIn(String brIn) {
		this.brIn = brIn;
	}
	public String getBrUp() {
		return brUp;
	}
	public void setBrUp(String brUp) {
		this.brUp = brUp;
	}
	public String getBrDe() {
		return brDe;
	}
	public void setBrDe(String brDe) {
		this.brDe = brDe;
	}
	
}
